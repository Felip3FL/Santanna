//3) Imprima todos os múltiplos de 3, entre 1 e 100.

package caeum.captulo03_variaveisLoops.exercicio3_3;

public class MeuPrograma3 {

		public static void main(String[] args) {
			int i,j;
			for (i=1;i<=100;i++){
				j = 3;
				j=j*i;
				System.out.println("3 x "+i+" = "+j);
			}
		}
}
		


	

