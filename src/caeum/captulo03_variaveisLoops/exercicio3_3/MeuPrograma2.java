package caeum.captulo03_variaveisLoops.exercicio3_3;

class MeuPrograma2 {
	
	public static void main(String[] args) {
		int i,j;
		for (i=1;i<=5;i++){
			j = 1;
			j=j+i;
			System.out.println("1 + "+i+" = "+j);
		}
	 }
 }